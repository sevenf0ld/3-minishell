#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char	*str;
	int		i;

	str = malloc(n + 1);
	i = -1;
	while (i < n && s[++i] != '\0')
		str[i] = s[i];
	str[i] = '\0';
	return (str);
}
