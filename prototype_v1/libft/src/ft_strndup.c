#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char *str = malloc(n + 1);
	int i;
	for (i = 0; i < n && s[i] != '\0'; i++)
		str[i] = s[i];
	str[i] = '\0';
	return (str);
}
