#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*d;
	unsigned int	i;

	d = malloc((ft_strlen(s) + 1));
	if (!d)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
